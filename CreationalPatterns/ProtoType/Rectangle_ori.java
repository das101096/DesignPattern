package CreationalPatterns.ProtoType;

public class Rectangle_ori implements Shape_ori {
    private int startX, startY, width, height;

    public Rectangle_ori(int startX, int startY, int width, int height) {
        this.startX = startX;
        this.startY = startY;
        this.width = width;
        this.height = height;
    }

    public int getStartX() {
        return startX;
    }

    public void setStartX(int startX) {
        this.startX = startX;
    }

    public int getStartY() {
        return startY;
    }

    public void setStartY(int startY) {
        this.startY = startY;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    @Override
    public void whoAmI() {
        System.out.println("Rectangle {startX = " + startX + " startY = " + startY + " width = " + width + " height = "+ height +"}");
    }
}
